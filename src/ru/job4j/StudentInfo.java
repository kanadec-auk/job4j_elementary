package ru.job4j;

public class StudentInfo {
    public static void main (String[] args){
        for (int i=0; i<=9; i++)
        {
            for (int j=1; j<=1; j++)
            {
                System.out.print((i+j)+"*"+(j)+"="+((i+j)*j));

            }
            System.out.println();
        }
    }
}
